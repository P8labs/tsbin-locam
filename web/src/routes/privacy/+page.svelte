        camera feed is never recorded, transmitted, or stored.
      </p>
    </section>

    <section>
      <h2>Data Processing</h2>
      <p>
        When you scan a QR code, the image is processed locally to extract the
        encoded data. The decoded result is displayed to you and exists only in
        memory during your session.
      </p>
      <p>
        When you capture a photo, the image is saved to your device using your
        browser's download functionality. We never see or store these images.
      </p>
