      <p>
        Locam requires camera access to scan QR codes and capture photos. This
        permission is controlled by your browser, not by us.
      </p>
      <p>
        Your browser will ask for permission the first time you use Locam. You
        can revoke this permission at any time through your browser settings.
      </p>
      <p>
