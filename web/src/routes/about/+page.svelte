      <h2>About Locam</h2>
      <p>
        Locam is a privacy-first browser utility for QR code scanning and camera
        access.
      </p>
      <p>
        It was built in response to a simple problem: most QR scanning tools
        either require a phone or send your data to external servers.
      </p>
      <p>
        Locam takes a local-first approach. Everything runs in your browser. No
        data leaves your machine. No accounts. No tracking.
      </p>
      <p>
        The philosophy is straightforward: utility software should be fast,
        predictable, and respect your privacy by default.
      </p>
    </section>

    <section>
      <h2>Design Principles</h2>
      <p>Locam follows a set of core principles:</p>
      <ul>
        <li>Local processing only — no cloud dependencies</li>
        <li>Minimal permissions — only what's required</li>
        <li>Zero analytics — no tracking or telemetry</li>
        <li>Open source — transparent and auditable</li>
        <li>Small surface area — focused feature set</li>
      </ul>
      <p>These principles guide every technical decision.</p>
    </section>

